your_word = input("Enter a word: ")

def is_palindrome(word):
    return word == word[::-1]
    if word == your_word:
        is_palindrome(your_word) == True
    else:
        is_palindrome(your_word) == True
 
if is_palindrome(your_word) == True:
    print("Palindrome")
else:
    print("Not a Palindrome")
    

    

       
